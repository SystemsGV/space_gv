					style="color: #FFFFFF; font-size: 27px; vertical-align: middle; font-family: Montserrat, sans-serif; font-weight: 500; letter-spacing: -0.04em; text-align: center;"><strong>PROTOCOLO
						DE OBJETOS Y ALIMENTOS QUE NO INGRESAN AL PARQUE</strong></span></div><br />
			<p><b>OBJETOS PROHIBIDOS:</b><br />
			<p align="justify">
				1. Pelotas: No está permitido el ingreso de este objeto porque puede lastimar sin intención a algún
				visitante, o
				puede caer dentro de unos de los juegos ocasionando incidentes que lamentar.<br />
				2. Triciclos, patinetas, patines, carritos (todo objeto que sea manipulado o manejado sólo por el niño).
				Si el niño
				ingresa con estos juguetes puede lesionar al chocar a otros visitantes o lesionarse posiblemente a sí
				mismo al
				utilizarlo.<br />
				3. Armas de fuego: Por ser un establecimiento donde hay familias no está permitido el ingreso de armas,
				ya que
				pueden ser usadas de forma indebida.<br />
				4. Envases de vidrio: No están permitidos porque si caen y se rompen dentro del parque se considerarían
				un arma
				que podría ocasionar algún tipo de lesión grave a los visitantes.<br />
				5. Termos mayores a 750 ml y envases mayores a 1 litro: Estos son objetos contundentes que podrían ser
				utilizados como armas por el tamaño del envase.<br />
				6. Silbatos, trompetas, megáfonos u otros objetos de ruido: Estos objetos pueden generar molestia a
				otros visitantes y stress en los animales, acto que podría atentar contra su salud.<br />
				7. Cigarros, fósforos u objetos que generen fuego: Por estar en un ambiente con vegetación podría
				generar un incendio. En el caso de los visitantes que deseen fumar deberán hacerlo en los ambientes
				habilitados para fumadores.<br />
				8. Tijeras, navajas, cuchillos u otros objetos punzo cortantes: Estos objetos son considerados armas
				blancas y pueden ocasionar lesiones graves o mortales, están terminantemente prohibidos en nuestros
				parques.<br />
				9. Palos de Selfie: Por motivos de seguridad no ingresa palos de selfie ya que este dispositivo tiene un
				brazo que se extiende y puede ocasionar incidentes en el parque.<br />
				10. Drones: Este dispositivo no está permitido su ingreso al parque ya que al ser usado podría caer o
				chocar en los juegos mecánicos o zonas donde tenemos visitantes.<br />

			<p><b>ALIMENTOS Y BEBIDAS</b><br />
			<p align="justify">
				1. No está permitido el ingreso de alimentos y bebidas en general, ya que una vez que el visitante haya
				ingresado la empresa se hace responsable de su seguridad dentro del parque. Solo en el caso del ingreso
				de agua, está permitido ingresar botellas plásticas SELLADAS máximo de 1 litro (una botella por
				persona). Por seguridad botellas de agua selladas más grandes de un 1 litro, no podrá ingresar al
				parque.<br />
				2. La manipulación y conservación de los alimentos que ingresa de manera externa no puede ser
				garantizada por la empresa, por la tanto, la empresa no podría asumir responsabilidad por visitantes que
				ingresen alimentos si no conocemos el real estado de conservación de los mismos.<br />
				3. Si hay un alimento especial para una persona que tiene un problema de salud, se activa el protocolo
				de ingreso de alimentos especiales. <br />
				4. No pueden ingresar frutas, verduras ni productos perecibles, por ser alimentos con alto riesgo de
				putrefacción. En el caso de las frutas que tienen cáscara y que deben ser pelados antes de su consumo
				como el plátano, mandarinas, etc. Solo pueden ingresar al parque sin cáscara y en la presentación de
				papilla para el consumo del bebe. Estos alimentos no podrán ingresar con cáscara porque si no se cumple
				el correcto desecho de la cáscara podría ocasionar algún tipo de lesión involuntaria a los demás
				visitantes. Para los visitantes que soliciten una excepción por salud para el ingreso con cáscara de
				dicho alimento se activa el protocolo de alimentos especiales, los cuales son resguardados en la cocina
				de nuestras instalaciones, aquí es donde podrá pelar el alimento para su adecuado consumo y así
				garantizar el correcto desecho de las cáscaras de la fruta. La empresa no puede garantizar el estado de
				conservación de los alimentos, los cuales podría originar algún tipo de intoxicación a quienes lo
				ingieran.<br />
				5. Debe tenerse en cuenta, que en este caso, el parque alberga en el zoológico y en la granja animales
				que interactuan con los visitantes a través del alimento, el consumo para los animales de alimentos no
				adecuados, puede representar un riesgo para ellos y en el caso especifico del zoológico que alberga
				especies de fauna silvestre, recibidos en calidad de custodia por la Administración Técnica Forestal y
				de Fauna Silvestre del Ministerio de Agricultura, por los cuales debemos dar cuenta de su bienestar,
				ante dicha autoridad, tenemos una gran responsabilidad frente al estado de custodiar su salud, lo cual
				justifica nuestras políticas de restricción sobre el ingreso de alimentos.<br /><br />

