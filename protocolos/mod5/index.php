			<div style="background-color:#FF9900; border-radius: 8px;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span style="color: #FFFFFF; font-size: 27px; vertical-align: middle; font-family: Montserrat, sans-serif; font-weight: 500; letter-spacing: -0.04em; text-align: center;"><strong>POLÍTICAS DEL PARQUE</strong></span></div><br />
			<p><b>Código de conducta para los visitantes de La Granja Villa</b><br />
			<p align="justify">Para garantizar que todos nuestros visitantes tengan una experiencia segura y confortable, por favor
				siga las normativas del parque. Los visitantes que “no cumplen las normativas” tienen un impacto
				negativo en la seguridad y diversión de los demás, y se les puede pedir abandonar el parque sin
				reembolso alguno.
				¡Agradecemos su comprensión para hacer un día satisfactorio para todos!.<br /><br />

				<b>NORMATIVAS DEL PARQUE</b><br /><br />
				COMPORTAMIENTO: Se espera de todos nuestros visitantes un comportamiento apropiado de
				acuerdo al ambiente familiar y confortable que tenemos en el parque. Quedan estrictamente
				prohibidas las conductas negativas, indisciplinadas, perjudiciales u ofensivas, incluyendo no
				respetar las filas o separar lugares. No se recomienda utilizar palabras altisonantes, lenguaje o
				señales inapropiados.<br /><br />

				CÓDIGO DE VESTIMENTA: La Granja Villa implementa un código de vestimenta de acuerdo con la
				atmósfera familiar y de cordialidad. En todo momento se deberá vestir ropa en las instalaciones de La
				Granja Villa, la misma que no deberá contener imágenes que inciten al odio o a la violencia.
				Solo en la zona de piscinas (temporada de verano) se permite el uso de ropa de baño.<br /><br />

				NO FUMAR: Únicamente se podrá fumar en las áreas específicamente establecidas para este efecto
				sin excepciones (incluidos cigarros electrónicos).<br /><br />

				RESTRICCIONES: No está permitido el ingreso de alimentos y bebidas en general, ya que una vez
				que el visitante haya ingresado al parque, la empresa se hace responsable de su seguridad dentro
				del mismo. La manipulación y conservación de los alimentos que ingresa de manera externa, no
				puede ser garantizada por la empresa, por lo tanto, la empresa no podría asumir responsabilidad
				alguna por visitantes que ingresen alimentos si no conocemos el estado de conservación de los
				mismos.<br />
				Si hay un alimento especial para una persona que tiene un problema de salud, se activará el protocolo
				de ingreso de alimentos especiales.
				Por seguridad, no se permite el ingreso de mascotas ni productos tales como cooler, termos, pelotas,
				triciclos y envases de vidrios.<br />
				Como visitantes se les pedirá pasar por un punto de revisión donde se verificará que no ingresen
				armas de fuego u objetos punzocortantes. Cualquier artículo que no esté permitido en el parque se
				deberá devolver a su vehículo, de no contar con el tiene la opción de utilizar los lockers que se
				encuentran en el ingreso al parque y tienen un costo de S/. 1.00 nuevo sol (leer las instrucciones de
				uso que se encuentran en ellos).<br />
				Cámaras fotográficas o de vídeo, así como celulares no están permitidos en los juegos.
				Visitantes con yesos, prótesis, fajas y collarines, no podrán ingresar a ciertos juegos basados en los
				requerimientos establecidos por el fabricante. Las guías de seguridad de cada juego las podrá
				obtener en atención al cliente (al ingreso del parque) o en la web.<br />
				Nuestros visitantes no podrán ingresar a áreas restringidas, esto incluye juegos o zonas señalizadas
				como tales.<br />
				Cualquier artículo perdido en el área de un juego deberá ser reportado al operador del mismo. Para
				artículos extraviados o recuperados, acudir a la caja principal del restaurante LA PALAPA.<br /><br />

				COMPRA DE ALCOHOL: Para la venta de alcohol se solicitara el DNI o documento que sustente la
				mayoría de edad. Solo está permitido el consumo de tres bebidas alcohólicas por persona.<br /><br />

				ESTACIONAMIENTO: Para mayor seguridad de sus vehículos, agradeceremos respetar las
				normativas del estacionamiento, por ello es importante seguir las indicaciones del personal de
				seguridad quien lo orientara en la ubicación de sus vehículos según la hora de llegada
				<br /><br />


				<b>POLÍTICAS EN JUEGOS</b><br /><br />
				RESPONSABILIDAD DEL USUARIO: Existen riesgos inherentes a la participación en cualquier
				juego mecánico. Como visitante debe ejercer un buen juicio y actuar de manera responsable
				mientras hace uso de los juegos. Como visitante deberá obedecer todas las advertencias orales,
				escritas y hacer uso apropiado del equipo de seguridad proporcionado. Por favor consulte las guías
				de seguridad que se encuentran ubicadas en la entrada de cada juego.<br /><br />

				RESTRICCIONES EN JUEGOS: La seguridad es nuestra máxima prioridad. Visitantes que superen
				ciertas proporciones de peso y/o talla podrían no hacer uso de algunos juegos si los sistemas de
				seguridad no cierran de forma correcta. La información específica de los juegos estará disponible en
				Atención al cliente o en nuestra web.<br /><br />

				NO SUBIR A LAS BARANDAS: Está prohibido por cualquier motivo subirse a las Barandas pues esto
				puede ocasionar lesiones involuntarias. Está prohibido separar lugares en la fila.<br /><br />

				ARTÍCULOS: Los artículos sueltos (por ejemplo: lentes, mochilas, bolsas, gorras, etc.) no están
				permitidos en los juegos, deberán dejarse con alguien que no suba al juego o en un locker. La Granja
				Villa y sus empleados no se hacen responsables por pérdida o robo de artículos.<br /><br />

				INCLEMENCIAS DEL TIEMPO: Los juegos podrían cerrar temporalmente durante lluvia o fuertes
				vientos. Los juegos reabrirán tan pronto como las condiciones de operación sean seguras. No hay
				reembolsos por lluvia o inclemencias del tiempo.<br /><br />

				TÓPICO: Una enfermera titulada, estará ́ de guardia en nuestra Estación de Primeros Auxilios. Si
				requiere asistencia médica solicítelo a alguń empleado del parque y este pedirá ́el servicio. Consulte
				el mapa del parque para su ubicacióńn exacta. Toda lesión deberá ser reportada al Tópico antes de
				dejar el parque.
			</p><br /><br />

