			<p><b>DIETAS ESPECIALES:</b><br />
			<p align="justify">¡Estamos felices de que hayas elegido La Granja Villa para pasar tu día! ¡Nuestro objetivo es hacerte vivir una experiencia entretenida y además educativa!.
				Por ello, es importante que leas estas indicaciones que te permitirán prepararte antes de ingresar a nuestro parque:<br /><br />

				1. Receta, documento o alguna indicación med́ ica donde indique la enfermedad que adolece*.<br />
				2. Firmar la carta de compromiso de dietas especiales, donde se indican los datos de la persona que tendrá la
				alimentacioń especial y la persona responsable del ingreso, con la descripcioń de los alimentos especiales que
				ingresa.<br />
				3. La lonchera será trasladada, conservada en refrigeración, calentada en microondas y servida en el restaurante
				Palapa o Cet́ ico, sobre un plato descartable, en el momento que los integrantes del grupo decidan toman su
				almuerzo o refrigerio previa coordinacioń y sin manipulacioń de envase para evitar contaminación cruzada.<br />
				4. No pueden ingresar envases de vidrio ni termos mayores a 750 ml por razones de seguridad**.<br />
				* En caso de no contar con receta o indicación med́ ica, deberá indicar en la carta de compromiso su condicioń de
				salud y seguir con el punto 2 del protocolo de ingreso de alimentos especiales.<br />
				** Véase en protocolos de objetos que no pueden ingresar.<br />


			<p><b>DIETAS DE BEBES:</b><br />
			<p align="justify">Se permite el ingreso de papillas para niños hasta el 1 año y medio de edad, toda papilla que ingrese debe tener
				las siguientes características:<br /><br />
				1. La papilla no puede ingresar en envase de vidrio.<br />
				2. La fruta debe ingresar sin cascara en un recipiente que no sea de vidrio.<br>
				3. La manipulación y conservación de los alimentos que ingresa de manera externa no puede ser garantizada por la empresa, por la tanto, la empresa no podría asumir responsabilidad por visitantes que ingresen alimentos si no conocemos el real estado de conservación de los mismos.<br />
				4. Los termos no ingresan y podrán solicitar agua caliente en forma gratuita en las cocinas de palapa y cetico.<br />
				5. En caso soliciten el ingreso de un termo a más de 750ml para el bebé por un motivo de salud, se
				activará el protocolo de ingreso de alimentos especiales.<br />
				6. Debe tenerse en cuenta, que en este caso, el parque alberga en el zoológico y en la granja animales que interactuan con los visitantes a través del alimento, el consumo para los animales de alimentos no adecuados, puede representar un riesgo para ellos y en el caso especifico del zoológico que alberga especies de fauna silvestre, recibidos en calidad de custodia por la Administración Técnica Forestal y de Fauna Silvestre del Ministerio de Agricultura, por los cuales debemos dar cuenta de su bienestar, ante dicha autoridad, tenemos una gran responsabilidad frente al estado de custodiar su salud, lo cual justifica nuestras políticas de restricción sobre el ingreso de alimentos.<br />

			<p><b>PASOS A SEGUIR PARA ACTIVACIÓN DE PROTOCOLO DE ALIMENTOS:</b><br />
			<p align="justify">
				1. El Visitante se acercará a ventanilla de Relaciones Públicas (ingreso al parque), donde indicará el motivo para
				poder activarle Protocolo.<br />
				2. En caso el Protocolo se active se firmará la Hoja de Responsabilidad por el alimento que ingresará al Parque
				(llenar datos completos).<br />
				3. El envase irá al Restaurant “Cetico”.<br />
				4. El personal del Restaurant lo servirá emplatado (no vidrio, no loza), proporcionado según la solicitud del
				Protocolo.<br />
				5. De quedar el envase (no vidrio, no loza) vacío, se entregará a visitante.<br />
				6. De ser un envase o contenido de vidrio o loza se quedara en el Restaurant hasta las 5:45 pm, después de
				dicha hora todos los envases irán a la ventanilla de Relaciones Públicas (ingreso al Parque) para poder ser
				recogidos por el visitante antes de retirarse.<br />
				7. Si el visitante desea retirarse del Parque antes de las 5:45 pm, deberá acercarse al Restaurant “Cetico” para
				indicar que le lleven el envase a la Ventanilla de Relaciones Públicas.</p><br /><br />

