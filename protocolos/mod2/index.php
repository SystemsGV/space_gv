			<p align="justify">Sólo está permitido el ingreso a las instalaciones del parque de Granja Villa Sur (en adelante el Parque) de menores de edad si están acompañados de un
				adulto responsable. Por excepción, un menor de edad podrá ingresar al Parque sin compañía de un adulto, sólo si es autorizado por la persona del área de Atención al Cliente y siguiendo el protocolo que se detalla líneas abajo:<br /><br />
				1. Se podrá autorizar el ingreso de un menor de edad al Parque, sin la compañía de un adulto, sólo si su edad fluctúa entre los 13 y 17 años, y siempre que exista una autorización expresa de sus padres o apoderados.<br />
				2. El padre o apoderado firmará un documento en donde se deja constancia su
				decisión de dejar al menor / menores en las instalaciones del Parque, sin compañía y
				bajo su total y absoluta responsabilidad. En dicho documento facilitará 2 números
				telefónicos como mínimo para que pueda ser localizado ante cualquier eventualidad.<br />
				3. Se le indicará a los menores de edad y al padre o apoderado, que no podrán
				ingresar a "las piscinas" o a cualquier otra actividad que requieran
				de la autorización de un adulto.<br />
				4. En caso los menores de edad vengan solos deberán proporcionar los teléfonos
				del padre o apoderado para que autorice el ingreso. A su vez, el padre o apoderado
				deberá hacer llegar la autorización escrita por un medio electrónico.<br />
				5. Una vez culminado este protocolo, se autorizará el cobro en caja.<br />
