				escritas de nuestro personal del parque.<br />
				17. Por favor utilice los baños y enseñe a sus hijos el buen uso que se les debe dar.<br />
				18. No ingerir alimentos ni bebidas dentro de las piscinas.<br />
				19. La administración esta facultada a retirar sin excepción a aquellas personas que atenten contra la moral,
