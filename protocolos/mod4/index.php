				3. Solo podrán ingresar sin pulsera los niños menores a 0.80 cm de estatura.<br />
				4. No ingresar a la piscina bajo los efectos de alcohol, drogas alucinógenas o medicamentos que disminuyan su
				capacidad física.<br />
				5. No ingrese a la piscina si tiene algún tipo de laceración, heridas o enfermedades infecto contagiosas.<br />
				6. Ducharse antes de ingresar a la piscina.<br />
				7. Es importante no consumir alimentos una hora antes de ingresar a la piscina ya que puede perjudicar su salud.<br />
				8. Los alimentos adquiridos en los restaurantes solo pueden ser consumidos en las áreas de comida, por ningún
				motivo se les permitirá el ingreso a las piscinas con dichos alimentos (pollo, carnes, anticuchos, etc).<br />
				9. No ingrese a la piscina si tiene algún tipo de laceración, heridas o enfermedades infecto contagiosas.<br />
				10. Por seguridad no esta permitido el ingreso de colchonetas, flotadores o pelotas a la zona de piscinas.<br />
				11. Por respeto al prójimo no esta permitido el uso de lenguaje obsceno, lisuras y conducta decorosa.<br />
				12. No ingresar objetos que afecten la integridad de las demás personas.<br />
				13. Esta prohibido escupir, orinar o contaminar de alguna forma el área de piscina.<br />
				14. Absténgase de hacer juegos bruscos, clavados y maniobras peligrosas en la piscina ya que puede poner en
				riesgo su seguridad y la de los demás.<br />
				15. Por seguridad no correr por los bordes de las piscinas, podría poner en riesgo su integridad y la de los demás.<br />
